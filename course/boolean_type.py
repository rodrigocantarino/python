"""

Boolean type

Two constants: True or False

"""

value = True
value1 = False
print('value')
print(value)
print(type(value))
print('value1')
print(value1)
print(type(value1))

# Logic operators
print('Logic operators')
# To negate use 'not'
print('To negate use "not" value')
print(not value)
print(type(not value))

# 'Or' comparison operator
print("'Or' comparison operator")
print(value or value1)
print(type(value or value1))

# 'and' comparison operator
print("'And' comparison operator")
print(value and value1)
print(type(value and value1))

